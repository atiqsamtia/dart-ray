import 'package:ray/ray.dart';

extension RayColors on Ray{
  Ray green() {
    return this.color('green');
  }

  Ray orange() {
    return this.color('orange');
  }

  Ray red() {
    return this.color('red');
  }

  Ray purple() {
    return this.color('purple');
  }

  Ray blue() {
    return this.color('blue');
  }

  Ray gray() {
    return this.color('gray');
  }
}
